    function dmha_13_generate_masked($PARAM_ID)
    {
        // ------------------------------------------------------------------------- INITIALIZE
            $isi    = '';
            
            $isi_model = dmha_15_let_me_generate_data_array(NULL,$PARAM_ID,'masked',null);
        // ------------------------------------------------------------------------- ACTION
            foreach ($isi_model as $row) {
                if($row->id == 15) { $isi .= ' $(".masked-'.$row->name.'").unmask().mask("9999999999999999"); '; }
                elseif($row->id == 17) { $isi .= ' $(".masked-'.$row->name.'").unmask().mask("99-99-9999"); '; }
                elseif($row->id == 20) { $isi .= ' $(".masked-'.$row->name.'").unmask().mask("999"); '; }
                elseif($row->id == 21) { $isi .= ' $(".masked-'.$row->name.'").unmask().mask("999"); '; }
                elseif($row->id == 50) { $isi .= ' $(".masked-'.$row->name.'").unmask().mask("99-99-9999"); '; }
                elseif($row->id == 55) { $isi .= ' $(".masked-'.$row->name.'").unmask().mask("99.99.999.999.999-9999.9"); '; }
            } 
        // ------------------------------------------------------------------------- SEND
            $word = $isi;
            return $word;
		//////////////////////////////////////////////////////////////////////////// 		
    }

